	Name NVARCHAR(32) UNIQUE NOT NULL, -- THE NAME OF THE ALGORITHM
	HandlerClass NVARCHAR(MAX), -- THE HANDLER CLASS WHICH GENERATES THE CODE
	CONSTRAINT PK_PhoneticAlgorithm PRIMARY KEY (PhoneticAlgorithmId)
);

