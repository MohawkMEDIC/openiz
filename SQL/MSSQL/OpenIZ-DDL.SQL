CREATE TABLE Protocol
(
	ProtocolId UNIQUEIDENTIFIER NOT NULL DEFAULT NEWID(), -- UNIQUE IDENTIFIER FOR THE Protocol
	Name NVARCHAR(64) NOT NULL, -- NAME FOR THE PROTOCOL (EX: CHILDHOOD VACCINATIONS, TWINWRIX SCHEDULE)
