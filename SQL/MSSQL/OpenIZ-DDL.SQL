	CreatedBy UNIQUEIDENTIFIER NOT NULL, -- THE USER WHICH CREATED THE PROTOCOL
	ObsoletionTime DATETIMEOFFSET, -- THE TIME THE PROTOCOL WAS OBSOLETED
