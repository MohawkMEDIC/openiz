	CreationTime DATETIMEOFFSET NOT NULL DEFAULT CURRENT_TIMESTAMP, -- THE CREATION TIME OF THE APPLICATION
	CreatedBy UNIQUEIDENTIFIER NOT NULL, -- THE USER THAT CREATED THE APPLICATION
